class BMExercicio02 {
    public static void main(String[] args) {

        int i;
        i = 1;
        System.out.println(i);

        long j;
        j = 2;
        System.out.println(j);

        float p;
        p = 20.0f;
        System.out.println(p);

        double q;
        q = 30.0;
        System.out.println(q);

        boolean b;
        b = true;
        System.out.println(b);

        char c;
        c = 'k';
        System.out.println(c);

    }
}
